import java.util.List;

@JsonIgnoreProperties(ignoreUnknown = true)
public class CheckmkHostsResponse {

    @JsonProperty("value")
    private List<Host> hosts;

    public List<Host> getHosts() {
        return hosts;
    }

    public void setHosts(List<Host> hosts) {
        this.hosts = hosts;
    }
}
